import math

# Mais alguns exemplos do funções da biblioteca matemática
a = math.sqrt(25)  # Raiz quadrada
b = math.cbrt(27)  # Raiz cúbica
c = math.ceil(3.5)  # Próximo inteiro
d = math.floor(3.5)  # Inteiro anterior
e = math.pow(2, 3)  # Potência
f = math.factorial(5)  # Fatorial

print(a)
print(b)
print(c)
print(d)
print(e)
print(f)
